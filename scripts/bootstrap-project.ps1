<#
  PCC new-project bootstrap (COCKPIT_ROADMAP #18).

  Scaffolds a brand-new project "the PCC way" so every project starts identical:
  the cockpit app, the self-contained detector/lifecycle/handoff scripts, fresh
  declared state (lifecycle pinned at 'define', generic boundaries), and starter
  brief/rules/decisions. Then optionally git-inits it.

  Deterministic: file copies + templated text, no LLM. Read-only against THIS
  repo (the reference implementation); only writes under -Target.

  Usage:
    pwsh -File scripts/bootstrap-project.ps1 -Target C:\path\to\new-project -Name "My Project"
    (add -Force to write into a non-empty folder; -NoGit to skip git init)
#>
param(
  [Parameter(Mandatory = $true)][string]$Target,
  [Parameter(Mandatory = $true)][string]$Name,
  [string]$Blueprint,
  [switch]$Force,
  [switch]$NoGit,
  [switch]$NoInbox   # skip the scaffolded-inbox drop (the caller registers the project directly)
)

$ErrorActionPreference = 'Stop'
$src = Split-Path -Parent $PSScriptRoot

# Optional blueprint (from the chat intake, reusing CCB's wizard shape). When
# present, the brief and first decision are filled from it instead of the
# generic template.
$bp = $null
if ($Blueprint) {
  if (-not (Test-Path -LiteralPath $Blueprint -PathType Leaf)) { Write-Error "Blueprint not found: $Blueprint"; exit 1 }
  try { $bp = Get-Content -Raw -LiteralPath $Blueprint | ConvertFrom-Json } catch { Write-Error "Blueprint is not valid JSON: $($_.Exception.Message)"; exit 1 }
}

# --- validate target ---
if (Test-Path -LiteralPath $Target) {
  $existing = @(Get-ChildItem -LiteralPath $Target -Force)
  if ($existing.Count -gt 0 -and -not $Force) {
    Write-Error "Target '$Target' is not empty. Use -Force to write into it anyway."
    exit 1
  }
} else {
  New-Item -ItemType Directory -Path $Target -Force | Out-Null
}
$Target = (Resolve-Path -LiteralPath $Target).Path
Write-Output "Bootstrapping '$Name' into $Target"

function Ensure-Dir([string]$p) { if (-not (Test-Path -LiteralPath $p)) { New-Item -ItemType Directory -Path $p -Force | Out-Null } }
function Copy-File([string]$rel) {
  $from = Join-Path $src $rel
  if (-not (Test-Path -LiteralPath $from)) { Write-Output "  (skip, missing: $rel)"; return }
  $to = Join-Path $Target $rel
  Ensure-Dir (Split-Path -Parent $to)
  Copy-Item -LiteralPath $from -Destination $to -Force
  Write-Output "  + $rel"
}

# --- 1. the cockpit app, WITHOUT node_modules (large; regenerated by npm install)
#        and without the dev repo's local verification record. Excluded up front
#        rather than copy-then-delete, so bootstrap stays fast. ---
Write-Output "Copying app/ (excluding node_modules) ..."
$appSrc = Join-Path $src 'app'
$appDst = Join-Path $Target 'app'
Ensure-Dir $appDst
Get-ChildItem -LiteralPath $appSrc -Force | Where-Object { $_.Name -ne 'node_modules' } | ForEach-Object {
  Copy-Item -LiteralPath $_.FullName -Destination $appDst -Recurse -Force
}
$lv = Join-Path $appDst 'last-verification.txt'
if (Test-Path -LiteralPath $lv) { Remove-Item -LiteralPath $lv -Force }

# --- 2. the full cockpit engine, copied WHOLESALE so every project is born
#        identical and bulletproof-by-default (DECISION-106). A hand-picked script
#        list silently drifted: new scripts (e.g. the lifecycle-advance gate,
#        high-stakes/doctor) AND the CI + pre-commit guardrails never got added, so
#        scaffolded projects had dead features and no test gate. Copying the whole
#        engine — every detector/lifecycle/verify/backup script, their schemas, the
#        CI workflow, and the pre-commit test gate — fixes that at the root and
#        keeps any FUTURE script travel automatically, no list to maintain. ---
Write-Output "Copying the cockpit engine (scripts, schemas, CI workflow, pre-commit gate) ..."
foreach ($dir in @('scripts', 'schemas', '.github', '.githooks')) {
  $from = Join-Path $src $dir
  if (Test-Path -LiteralPath $from) {
    Copy-Item -LiteralPath $from -Destination $Target -Recurse -Force
    Write-Output "  + $dir/ (whole)"
  } else {
    Write-Output "  (skip, missing: $dir/)"
  }
}

# --- 3. config / rules ---
Copy-File 'CLAUDE.md'
Copy-File 'AGENTS.md'
Copy-File '.gitignore'
Copy-File 'docs/BACKUP_POLICY.md'   # the mandatory backup policy CLAUDE.md + the copied backup script reference
Copy-File 'docs/DECISION_AND_CHANGE_STANDARD.md'  # the locked decision + change-rollout standard (ADR format + Confirmation/Engagement)
Copy-File 'docs/TRUST_CALIBRATION.md'             # owner-facing "how much to trust a done" guide
Copy-File 'docs/specs/README.md'                  # the lean feature-spec standard + template

# --- 4. generic declared state (lifecycle model + thresholds copied; state fresh) ---
Copy-File '.cockpit/state/lifecycle-model.json'
# The model switcher list is generic (no project-specific data), so COPY the current one rather
# than templating a stale copy — every new project is born with the same up-to-date model list and
# default the home cockpit has (fix for under-provisioning: children were born without models.json
# and fell back to a hard-coded single-model list the owner couldn't edit).
Copy-File '.cockpit/state/models.json'
# NOTE: bloat-thresholds.json is NOT copied from PCC (soak fix F10). PCC's globs point
# at PCC's own engine (app/, scripts/), so a copied config made the child flag the
# copied engine as the owner's bloat. A product-scoped config is generated below.
Ensure-Dir (Join-Path $Target '.cockpit/result/detections')
Ensure-Dir (Join-Path $Target '.cockpit/logs')

$stamp = (Get-Date).ToString('yyyy-MM-ddTHH:mm:sszzz')

Set-Content -LiteralPath (Join-Path $Target '.cockpit/state/lifecycle-state.json') -Encoding utf8 -Value (@"
{
  "lane": "main",
  "current_stage": "define",
  "note": "New project bootstrapped by PCC. Start by defining the goal and rough scope. Set deliberately, not inferred.",
  "updated_at": "$stamp"
}
"@)

Set-Content -LiteralPath (Join-Path $Target '.cockpit/state/app-build-scope.json') -Encoding utf8 -Value (@"
