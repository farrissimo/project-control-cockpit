# --- 6. git init ---
if (-not $NoGit) {
  Push-Location $Target
  try {
    & git init | Out-Null
    & git add -A | Out-Null
    # --no-verify: the pre-commit test gate now travels (.githooks/), but the new
    # project's node_modules isn't installed yet, so this first scaffold commit
    # must not be gated. The gate activates for the owner's real commits once they
    # run `npm install --prefix app` (postinstall wires core.hooksPath).
    & git -c user.name='PCC Bootstrap' -c user.email='pcc@localhost' commit -q --no-verify -m "Bootstrap $Name from PCC" | Out-Null
    # Tag the bootstrap commit as the drift/stale-docs baseline (soak fix F9). A
    # fresh project has no 'main' branch, so the detectors' baseline must be a ref
    # that actually exists. A tag is stable, branch-name-independent, and marks
    # exactly "the point the owner's work started from" -- so drift compares
    # everything built since bootstrap, not an incomplete working-tree-only diff.
    & git tag pcc-baseline | Out-Null
    Write-Output "git: initialized, made the first commit, and tagged it 'pcc-baseline' (drift/stale-docs baseline)."
  } catch { Write-Output "git: skipped ($($_.Exception.Message))" }
  Pop-Location
}

# --- register with the home cockpit (file bridge) ---
# Drop the new project's path into an inbox in the repo this script was launched
# from, so the running cockpit auto-imports it into its project switcher (the app
# clears the inbox after reading). This is what makes "New project" appear in the
# switcher without a manual "Open existing project" step. -NoInbox skips this when the caller
# (e.g. the DECISION-114 Save Project flow) registers the new project directly.
if (-not $NoInbox) {
  try {
    $inboxPath = Join-Path $src '.cockpit/state/scaffolded-inbox.json'
    $inbox = @()
    if (Test-Path -LiteralPath $inboxPath) { try { $inbox = @(Get-Content -Raw -LiteralPath $inboxPath | ConvertFrom-Json) } catch { $inbox = @() } }
    if ($inbox -notcontains $Target) { $inbox += $Target }
    ($inbox | ConvertTo-Json -AsArray) | Out-File -FilePath $inboxPath -Encoding utf8
  } catch { }
}

Write-Output ''
Write-Output "Done. Next steps:"
