"@)

# --- vision promises (owner intent for the Owner Overview, DECISION-107). NEVER
#     copy PCC's own promises; generate FRESH per project. Use blueprint promises
#     only if the intake supplied owner-approved ones; otherwise a clearly-incomplete
#     placeholder that says it needs owner review. We never invent final promises. ---
$promises = $null
if ($bp -and $bp.vision_promises) { $promises = @($bp.vision_promises) }
if (-not $promises -or $promises.Count -eq 0) {
  $promises = @(
    [ordered]@{ id = 'primary-outcome'; label = "(Define $Name's primary outcome)"; declared_status = 'needs_owner_review'; evidence = ''; not_proven = 'Vision promises have not been reviewed by the owner yet.' }
  )
}
$visionObj = [ordered]@{
  version       = 1
  project       = $Name
  last_reviewed = $null
  review_status = 'needs_owner_review'
  note          = 'Owner-declared project intent, shown as cards in the Owner Overview. PLACEHOLDER until the owner reviews/replaces them. declared_status is self-assessment, not machine proof.'
  promises      = $promises
}
Set-Content -LiteralPath (Join-Path $Target '.cockpit/state/vision-promises.json') -Encoding utf8 -Value ($visionObj | ConvertTo-Json -Depth 6)

# --- 5. starter docs (filled from the blueprint when present) ---
Ensure-Dir (Join-Path $Target 'docs')

function BpList($arr) { if ($arr) { return (($arr | ForEach-Object { "- $_" }) -join "`n") } return '' }

if ($bp -and $bp.project) {
  $p = $bp.project
  $whatThis = "$Name — $($p.problem_statement)`n`nFor: $($p.target_user)`n`nDone looks like: $($p.desired_outcome)"
  if ($p.hard_constraints) { $whatThis += "`n`nHard constraints: $($p.hard_constraints)" }
  $inScope = BpList $bp.scope.in_scope
  $outScope = BpList $bp.scope.out_of_scope
  $riskLines = ''
  if ($bp.scope.risks) { $riskLines = (($bp.scope.risks | ForEach-Object { "- [$($_.severity)] $($_.description) -> $($_.mitigation)" }) -join "`n") }
  $scopeBlock = "`n`n## Scope`n**In:**`n$inScope`n`n**Out:**`n$outScope"
  if ($riskLines) { $scopeBlock += "`n`n**Risks:**`n$riskLines" }
  $decisionBody = "Project defined via the PCC chat intake. Problem: $($p.problem_statement) Outcome: $($p.desired_outcome) Risk tolerance: $($p.risk_tolerance). Check-in: $($p.preferred_stopping_point)."
} else {
  $whatThis = "$Name. (Define the goal and what ""done"" looks like here.)"
  $scopeBlock = ''
  $decisionBody = "This project was scaffolded from the PCC reference implementation: cockpit app, deterministic detectors, lifecycle state-machine, and honest-signal design."
}

