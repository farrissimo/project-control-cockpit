// Write the build stamp (app/build-info.json) — runs at package time, before electron-builder.
//
// A packaged app has no .git inside it, so the ONLY way it can name the commit it was built
// from is if we record that at build time. This is that step. It is wired into `npm run dist`
// so a packaged app can never ship without an identity (spec AC-6).
//
// Fails CLOSED: if git cannot name the commit, this exits nonzero and the build stops rather
// than shipping an unidentifiable app. That is deliberate — an app that cannot say which code
// it is running is the problem this slice exists to fix, so silently producing one would be
// self-defeating.
//
// buildStamp() is exported and pure so the writer/reader contract is round-trip tested
// (app/tests/unit/build-identity.test.js): the shape this WRITES must be a shape
// build-identity.js ACCEPTS, or a packaged app reports "unknown" and nobody finds out
// until an owner is staring at it.
//
// Spec: docs/specs/build-identity.md

const fs = require('fs');
const path = require('path');
const { execFileSync } = require('child_process');

const APP_DIR = path.join(__dirname, '..');
const REPO_DIR = path.join(APP_DIR, '..');
const STAMP_PATH = path.join(APP_DIR, 'build-info.json');

/**
 * Build the stamp object. Pure — every input injected, no clock, no git, no fs.
 * @param {{version: string, sha: string, dirty: boolean, now: Date}} f
 */
function buildStamp(f) {
  if (typeof f.version !== 'string' || !f.version) throw new Error('version must be a non-empty string');
  if (!/^[0-9a-f]{40}$/i.test(f.sha || '')) throw new Error('sha must be a 40-char commit sha, got: ' + JSON.stringify(f.sha));
  if (typeof f.dirty !== 'boolean') throw new Error('dirty must be a boolean');
  if (!(f.now instanceof Date) || Number.isNaN(f.now.getTime())) throw new Error('now must be a valid Date');
  return {
    version: f.version,
    sha: f.sha.toLowerCase(),
    builtAt: f.now.toISOString(), // always UTC ISO-8601
    dirty: f.dirty,
    note: 'Generated by app/tools/write-build-info.js at package time. Build artifact — not committed. See docs/specs/build-identity.md',
  };
}

function main() {
  const git = (args) => execFileSync('git', args, { cwd: REPO_DIR, encoding: 'utf8', windowsHide: true }).trim();
  const fail = (msg) => {
    console.error('[FAIL] build stamp: ' + msg);
    console.error('       Refusing to package an app that cannot identify its own commit.');
    process.exit(1);
  };

  let sha, dirty, version;
  try { sha = git(['rev-parse', 'HEAD']); }
  catch (e) { return fail('git could not resolve HEAD (' + e.message + ')'); }

  // Dirty is recorded, not blocked on. Packaging a dirty tree is sometimes legitimate (a local
  // trial build); what is NOT legitimate is that build later claiming to BE the commit. The app
  // surfaces dirty as "not identifiable by commit" instead of hiding it.
  try { dirty = git(['status', '--porcelain']).length > 0; }
  catch (e) { return fail('git could not determine whether the tree is clean (' + e.message + ')'); }

  try { version = JSON.parse(fs.readFileSync(path.join(APP_DIR, 'package.json'), 'utf8')).version; }
  catch (e) { return fail('could not read version from app/package.json (' + e.message + ')'); }

  let stamp;
  try { stamp = buildStamp({ version: version, sha: sha, dirty: dirty, now: new Date() }); }
  catch (e) { return fail(e.message); }

  fs.writeFileSync(STAMP_PATH, JSON.stringify(stamp, null, 2) + '\n', 'utf8');
  console.log('[OK] build stamp: ' + stamp.version + ' @ ' + stamp.sha.slice(0, 7)
    + (stamp.dirty ? ' (DIRTY TREE)' : '') + ' built ' + stamp.builtAt);
}

if (require.main === module) main();

module.exports = { buildStamp, STAMP_PATH };
